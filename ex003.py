algo = input('Digite qualquer coisa: ')
print('O tipo primito desse valor é: ', type(algo))
print('Só tem espaços? ', algo.isspace())
print('É um número? ', algo.isnumeric())
print('É um alfabético? ', algo.isalpha())
print('É um Alfanumerico? ', algo.isalnum())
print('Está em maisculo? ', algo.isupper())
print('Está em minusculo? ', algo.islower())
print('Está em capitalizada? ', algo.istitle)#Tem letras minusculas e maiscúla?
